#include "today.h"
#include <QApplication>
#include <QLocale>
#include <QTranslator>

int main(int argc, char *argv[])
{
    QApplication a(argc, argv);

    QString locale = QLocale::system().name();

    QTranslator translator;
    translator.load(QString("today_") + locale);
    a.installTranslator(&translator);

    Today w;
    w.show();
    
    return a.exec();
}
